package com.yangxianwen.post176.bean;

public class Meal {

    private String cName;
    private String cFoodCode;
    private String cDate;
    private String cFoodName;
    private String cFoodType;
    private String cStartTime;
    private String cEndTime;
    private double nSum;
    private double nReferenceValue;

    public String getCName() {
        return cName;
    }

    public void setCName(String cName) {
        this.cName = cName;
    }

    public String getCFoodCode() {
        return cFoodCode;
    }

    public void setCFoodCode(String cFoodCode) {
        this.cFoodCode = cFoodCode;
    }

    public String getCDate() {
        return cDate;
    }

    public void setCDate(String cDate) {
        this.cDate = cDate;
    }

    public String getCFoodName() {
        return cFoodName;
    }

    public void setCFoodName(String cFoodName) {
        this.cFoodName = cFoodName;
    }

    public String getCFoodType() {
        return cFoodType;
    }

    public void setCFoodType(String cFoodType) {
        this.cFoodType = cFoodType;
    }

    public String getCStartTime() {
        return cStartTime;
    }

    public void setCStartTime(String cStartTime) {
        this.cStartTime = cStartTime;
    }

    public String getCEndTime() {
        return cEndTime;
    }

    public void setCEndTime(String cEndTime) {
        this.cEndTime = cEndTime;
    }

    public double getNSum() {
        return nSum;
    }

    public void setNSum(double nSum) {
        this.nSum = nSum;
    }

    public double getNReferenceValue() {
        return nReferenceValue;
    }

    public void setNReferenceValue(double nReferenceValue) {
        this.nReferenceValue = nReferenceValue;
    }
}
